package DomainModel;

public class SubeMuduru {

    private String mudurID;
    private String mudurAdi;
    private String mudurSoyadi;
    private String parola;
    private Sube sube;

    public String getMudurID() {
        return mudurID;
    }

    public void setMudurID(String mudurID) {
        this.mudurID = mudurID;
    }

    public String getMudurAdi() {
        return mudurAdi;
    }

    public void setMudurAdi(String mudurAdi) {
        this.mudurAdi = mudurAdi;
    }

    public String getMudurSoyadi() {
        return mudurSoyadi;
    }

    public void setMudurSoyadi(String mudurSoyadi) {
        this.mudurSoyadi = mudurSoyadi;
    }

    public String getParola() {
        return parola;
    }

    public void setParola(String parola) {
        this.parola = parola;
    }

    public Sube getSube() {
        return sube;
    }

    public void setSube(Sube sube) {
        this.sube = sube;
    }
    

}
